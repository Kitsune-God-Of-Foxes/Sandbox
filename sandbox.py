def main() -> None:
  N = int(input())
  compOG = input()
  number, S, W, P, C = compOG.split()
  Value = S + W + P + C
  for i in range: N
  altcomp = input()
  altnumber, altS, altW, altP, altC, = altcomp.split()
  Altvalue = altS + altW + altP + altC
  if Value + 1.2 < Altvalue:
    Value = Altvalue
    number = altnumber
  print(number, Value)

if __name__ == "__main__":
  main()
